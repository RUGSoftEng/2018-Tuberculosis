	'Wat is het verschil tussen actieve tuberculose en latente tuberculose?',
	'Voor actieve tuberculose geldt dat een persoon zich vaak ziek voelt. Bij latente tuberculose voelt de persoon zich vaak niet ziek.
Symptomen die ervaart worden bij actieve tuberculose zijn: Droge hoest voor langer dan 3 weken, nachtzweten, pijn op de borst, slijm of bloed ophoesten, zwakte, vermoeidheid, gewichtsverlies, rillingen, verminderde eetlust en koorts. Iemand met latente tuberculose ervaart geen symptomen. Daarnaast is een persoon met actieve tuberculose besmettelijk en is er altijd een behandeling nodig. Bij latente tuberculose is de persoon niet besmettelijk en er is slechts in bepaalde gevallen een behandeling nodig.'
